CREATE OR REPLACE FUNCTION "public"."handle_delete_user"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$BEGIN

  delete from public.game_state
  where id = old.id;

  -- Delete the user's profile
  delete from public.profiles
  where id = old.id;

  return old;

END;$$;


