CREATE OR REPLACE FUNCTION "public"."handle_new_user"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$begin
  -- Insert into the Public Table (Leaderboard Data)
  insert into public.profiles (
    id,
    username,
    points,
    rank
  )
  values (
    new.id,
    -- Pull the username/display name from the metadata provided during signup (e.g., from Google OAuth)
    coalesce(new.raw_user_meta_data ->> 'full_name', new.raw_user_meta_data ->> 'username', new.email),
    1000, -- Arbitrary initial points
    1
  );

  -- Insert into the Private Table (Game State/Secrets)
  insert into public.game_state (
    id,
    streak,
    hit_tolerance
  )
  values (
    new.id,
    0,     -- Initial streak is zero

    100    -- Default hit tolerance/mercy metric
  );

  return new;
end;$$;


