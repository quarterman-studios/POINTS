CREATE OR REPLACE FUNCTION "public"."calculate_bias"("attacker_points" integer, "defender_points" integer) RETURNS double precision
    LANGUAGE "plpgsql" IMMUTABLE
    AS $$
  declare
    k float := 3.0;
    D float;
  begin
    D := (attacker_points::float - defender_points::float) / defender_points::float;

    return 1.0 / (1.0 + exp(-1.0 * k * D));
  end;
$$;


ALTER FUNCTION "public"."calculate_bias"("attacker_points" integer, "defender_points" integer) OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."calculate_bias"("attacker_points" numeric, "defender_points" numeric) RETURNS double precision
    LANGUAGE "plpgsql" IMMUTABLE
    AS $$
  declare
    k double precision := 3.0;
    D double precision;
  begin
    D := (attacker_points::float - defender_points::float) / defender_points::float;

    if D > 5.0 then
      D := 5;
    end if;

    return 1.0 / (1.0 + exp(-1.0 * k * D));
  end;
$$;


